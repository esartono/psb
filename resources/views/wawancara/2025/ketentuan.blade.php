            <li>Ketentuan potongan khusus yang satu tidak dapat digabung dengan jenis potongan khusus lainnya</li>
            <li>Jenis Potongan Khusus sebagai berikut:
                <ol class="step1">
                    <li>
                        <div class="jarak"></div>
                        a. Potongan Saudara Kandung
                        <ol class="step2">
                            <li>Potongan Dana Pengembangan bagi calon siswa yang memiliki saudara kandung yang sedang bersekolah di SIT Nurul Fikri, sebagai berikut:
                                <ol class="step3">
                                    <li>Saudara kandung pertama mendapatkan potongan 5% dari Dana Pengembangan</li>
                                    <li>Saudara kandung kedua dan seterusnya mendapatkan potongan 10% dari Dana Pengembangan</li>
                                </ol>
                            </li>
                            <li>Potongan Dana Pengembangan bagi orangtua calon siswa yang mendaftarkan (sampai dengan daftar ulang lunas) lebih dari 1 calon siswa pada tahun pelajaran yang sama di SIT Nurul Fikri, masing-masing calon siswa mendapatkan potongan sebesar 10% dari Dana Pengembangan.</li>
                        </ol>
                    </li>
                    <li><div class="jarak"></div>b. Potongan Internal
                        <ol class="step2">
                            <li>Potongan biaya sebesar Rp. 5.000.000 dari dana pengembangan, bagi Pendaftar SDIT Nurul Fikri yang berasal dari TKIT Nurul Fikri.</li>
                            <li>Potongan biaya sebesar 10% dari dana pengembangan bagi calon siswa berasal dari Sekolah Islam Terpadu Nurul Fikri dan Nurul Fikri Boarding School Bogor.</li>
                        </ol>
                    </li>
                    <li><div class="jarak"></div>c. Potongan Prestasi
                        <ol class="step2">
                            <li>Potongan biaya 25% dari Dana Pengembangan dan bebas biaya pendaftaran bagi calon siswa yang berasal dari SIT Nurul Fikri dan NFBS Bogor yang mendapatkan undangan khusus dari Direktorat Pendidikan SIT Nurul Fikri.</li>
                            <li>Potongan biaya 50% dari Dana Pengembangan bagi calon siswa Pemenang Lomba <br>Tingkat Nasional (Berjenjang) Juara 1 dan 2, yang dibuktikan dengan sertifikat dan diverifikasi oleh panitia.</li>
                            <div class="page-break"></div>
                            <li>Potongan biaya sebesar 15% dari Dana Pengembangan bagi calon siswa yang memiliki hafalan Al-Qur’an, sebagai berikut:
                                <ol class="step3">
                                    <li>Pendaftar SD minimal 4 juz</li>
                                    <li>Pendaftar SMP minimal 8 juz</li>
                                    <li>Bagi siswa Pendaftar SMA minimal 10 juz</li>
                                </ol>
                            </li>
                            <li>Potongan biaya sebesar 50% dari Dana Pengembangan bagi calon siswa yang memiliki hafalan Al-Qur’an minimal 15 Juz dan sudah diverifikasi oleh SIT Nurul Fikri.</li>
                        </ol>
                    </li>
                    <li><div class="jarak"></div>d. <b>Ketentuan khusus potongan biaya penerimaan siswa baru tidak bersifat akumulatif</b></li>
                    <li><div class="jarak"></div>e. <b>Hasil keputusan panitia PPDB tidak dapat diganggu gugat</b></li>
                </ol>
            </li>
